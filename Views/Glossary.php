                    <h2 id="mindcontrol">Управление вниманием</h2>
                    <p class="lead">В практике постоянно используется управление вниманием. В асанах нужно направлять внимание в определённые точки, делается это обычно, когда асана освоена и всё внимание не тратится на удержание позы. В шавасане внимание перемещают
                        по телу. А есть и специализированные техники, такие как читдакаша дхарана или траттака. Траттака, это созерцание объекта, обычно пламя свечи, не моргая и не двигаясь. Если делать правильно восприятие изменяется.</p>
                    <p>Обычно техники работы с вниманием используются в статических положениях сидя или лёжа в шавасане. Однако, для успешного выполнения асан приходится научиться распределять внимание по телу, для удержания позы, что тренирует его.</p>
                    <hr class="featurette-divider">

                    <h2 id="bandhi">Бандхи</h2>
                    <p class="lead">Бандха, переводится как замок. Значение бандх трудно переоценить, уддияна бандха способна предотвратить множество болезней, за счёт того, что изменяет давление в полостях тела, форсируя прохождение жидкостей по сосудам и помогая сердцу
                        работать.
                        <p class="lead">Есть три основных замка:</p>
                        <h4>Мула бандха</h4>
                        <p>Сокращение лобково-копчиковой мышцы и сфинктера мочевого пузыря. Упрощённый вариант - все мышцы тазового дна.</p>
                        <h4>Уддияна бандха</h4>
                        <p>После полного выдоха, закрыв голосовые связки, подъём грудной клетки вверх, понижение давления в грудной клетке.</p>
                        <h4>Джаландхара бандха</h4>
                        <p>Напряжение мышц глотки и прижатие подбородка к груди.<br>Лёгкий вариант - небольшое движение подбородком вниз и назад, для воздействия на барорецепторы дуги аорты.</p>

                        <hr class="featurette-divider">

                        <h2 id="kriya">Крийи</h2>
                        <p class="lead">Крийя это по сути дела, некоторый набор действий. Дословно крийя — это практика. Может включать в себя разные техники разных видов йоги. Например, симха крийя, симхасана.</p>
                        <p>Техника выполнения симхасаны: Сидя в ваджрасане выполняется резкий выдох через открытый рот, одновременно высовывается язык и издаётся рычание, затем выполняется уддияна бандха, а высунутый язык прижимается к ярёмной впадине.</p>
                        <p>В разных школах йоги криям придаётся разное значение. Крийей может быть последовательность асан и пранаям для ежедневной практики или пение мантр и проведение ритуалов.</p>

                        <hr class="featurette-divider">

                        <h2 id="vyayama">Вьяяма</h2>
                        <p class="lead">Это просто суставная гимнастика. Есть мягкие вьяямы и жёсткие. Они так или иначе задействуют все суставы тела. Обычно выполняются в качестве разминки, могут использоваться как самостоятельная практика. Возможно создать тренировочную
                            программу только из вьяям.</p>
                        <p>Вьяямы получили распространение благодаря Свами Дхирендра Брахмачари. Который является одной из главных фигур в процессе распространения йоги в мире. Многим знакомы его ученики&nbsp;— Бал Мукунд Синдх и Рейнхард Гаммендхаллер.
                            Он издал книгу Йога-сукшма-вьяяма, а также вёл программу на индийском телевидении, во времена Индиры Ганди.</p>

                        <hr class="featurette-divider">

                        <h2 id="drishti">Дришти</h2>
                        <p class="lead">Направление взгляда. Известно, что направление взгляда в том числе перераспределяет мышечный тонус. Что позволяет, например, больше довернуться в скрутке.</p>
                        <p>Дришти имеют свои собственные названия, на обычном классе проще называть направление или точку, куда направить взгляд.</p>

                        <hr class="featurette-divider">

                        <h2 id="shatkarmy">Шаткарма</h2>
                        <p class="lead">Очистительные практики йоги. Например, джала нети и сутра нети, промывание носа водой и механическое очищение шнурком. Есть множество шаткарм, очищающих все части тела, включая слизистые поверхности желудка и кишечника. К шаткарме
                            можно отнести обычную чистку зубов и умывание.</p>
