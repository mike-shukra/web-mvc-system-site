                    <h2 id="asana">Асана</h2>
                    <p class="lead">Стаические положения тела в пространстве. Как простые позы сидя, так и сложные балансы на руках — это асаны. Существует множество асан. </p>
                    <p>Асана может являться мудрой, если выполняется определённым образом в течении определённого времени. </p>
                    <p>Асаны имеют идентификатор в виде названия на санскрите, а также, многие переведены на английский и русский язык. Кроме того, некоторые названия на русском никак не связаны с оригинальным названием на санскрите, просто они короче и
                        удобнее в использовании.</p>
                    <p> Одни и те же асаны могут называться по-разному в разных школах йоги. Есть большое количество вариантов многих из них. На одном классе может выполняться разное количество, в зависимости от школы или цели практики. В среднем от 4ёх до
                        50ти.
                    </p>
                    <hr class="featurette-divider">

                    <h2 id="vinyasa">Виньяса</h2>
                    <p class="lead">Динамические переходы между асанами, производимые на вдохе, на выдохе или задержке дыхания — это виньясы.</p>
                    <p>В некоторых школах виньясой называют переход именно из конкретно определённых поз, с заданным дыханием.</p>
                    <p>В большинстве направлений используются виньясы, где-то меньше и только на поздних этапах практики, где-то больше и на них делается акцент.</p>
                    <hr class="featurette-divider">

                    <h2 id="pranayama">Пранаяма</h2>
                    <p class="lead">То, что выполняется на йога классах, это подготовка к пранаяме, дыхательные упражнения. Направленные на развитие дыхательной мускулатуры, объёма лёгких, количество альвеол в лёгких, качество и количество кровеносных сосудов, а также
                        способности клеток обходиться меньшим количеством кислорода. Так называемая гипоксическая нагрузка. Что в сложных условиях даёт мышцам больше силы и выносливости, а уму, ясность.</p>
                    <p>Сама пранаяма предполагает визуализацию эзотерической системы представлений о реальности. Таких как прана, каналы нади, чакры и т.п. Это делается только под руководством гуру (духовного учителя, не путать с современным учителем йоги),
                        в противном случае это просто фантазия увлекающихся нью эйджем людей с хорошим воображением.</p>
                    <p class="lead">Несколько примеров упражнений:</p>
                    <h4>Уджайи</h4>
                    <p>Дыхание через слегка поджатые голосовые связки, точнее ложные голосовые связки, которые расположены сразу над истинными. Происходит с характерным шипящим звуком.</p>

                    <h4>Капалабхати</h4>
                    <p>Резкий шумный выдох через нос с помощью мышц пресса, вдох происходит беззвучно, автоматически при плавном отпускании мышц преса и как следствие, диафрагмы.</p>

                    <h4>Нади шодхана</h4>
                    <ul>
                        <li>Зажать большим пальцем правой руки правую ноздрю
                            <!--(для женщин - левой руки, левую ноздрю)-->и сделать полный вдох;</li>
                        <li>Отпустить правую ноздрю, зажать безымянным пальцем и мизинцем левую ноздрю и сделать полный выдох через правую ноздрю;</li>
                        <li>Не меняя положения пальцев руки, выполнить полный вдох через ту же правую ноздрю;</li>
                        <li>Зажать правую ноздрю, освободить левую и сделать через нее полный выдох;</li>
                        <li>Повторить.</li>
                    </ul>
                    <h4>Кумбхака (задержка дыхания)</h4>
                    <ul>
                        <li>после полного вдоха (антар кумбхака);</li>
                        <li>после полного выдоха (бахир кумбхака).</li>
                    </ul>
