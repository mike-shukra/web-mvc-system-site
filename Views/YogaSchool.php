                    <h2 id="">Введение</h2>
                    <p class="lead">Все представленные сегодня школы и направления появились не раньше 20ого века. Это не значит, что всё придумали недавно, просто ранее йога не была популяризирована. Если вы откроете любой древний текст по йоге, там везде указано, практиковать
                        тайно и хранить техники в секрете. Популяризация была сделана сознательно, с целью возрождения национальной индийской культуры, находящийся в упадке после оккупации сначала мусульманами, потом англичанами. Оккупация сильно подорвала
                        общественные устои и знания начали утрачиваться.</p>

                    <p class="lead">По большей части школы основаны на традиционной индийской системе ценностей. Однако на классах не преподают никакой философии или религии, хотя сами создатели направлений являлись их носителями.</p>
                    <p class="lead">Стоит отметить, что в мире и в местах Индии, где бывают туристы сейчас распространены курсы Американского Йога Альянса. Это образование в большинстве случаев очень низкого качества. Я был на уроках одного из таких курсов и видел, кого
                        и как они обучают. Такое образование не значит ничего, то есть человек с ним может иметь знания и опыт, только если он получил их ещё где-то.</p>

